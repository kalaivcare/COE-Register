    document.addEventListener("DOMContentLoaded", () => {
        const tracker = document.querySelector(".progress-tracker");
        const steps = tracker.querySelectorAll(
            "div:not(.progress_bar):not(.completed_bar):not(.pending_bar)"
        );
        const bars = tracker.querySelectorAll(
            ".progress_bar, .pending_bar, .completed_bar"
        );
        const nextBtn = document.querySelector(".nxt_btn");
        let currentStep = 0; // start from the first step

        nextBtn.addEventListener("click", () => {
            // if not at the last step
            if (currentStep < steps.length - 1) {
                const current = steps[currentStep];
                const next = steps[currentStep + 1];
                const bar = bars[currentStep];

                // current step becomes completed
                current.classList.remove("working", "pending");
                current.classList.add("completed");

                // current bar becomes completed
                if (bar) {
                    bar.classList.remove("pending_bar", "progress_bar");
                    bar.classList.add("completed_bar");
                }

                // next step becomes working
                next.classList.remove("pending");
                next.classList.add("working");

                // next bar (ahead of the next step) becomes progress (if exists)
                const nextBar = bars[currentStep + 1];
                if (nextBar) {
                    nextBar.classList.remove("pending_bar", "completed_bar");
                    nextBar.classList.add("progress_bar");
                }

                currentStep++;
            }
        });
    });
    </script>

